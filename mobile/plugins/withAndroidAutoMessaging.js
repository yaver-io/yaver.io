const {
  withAndroidManifest,
  withDangerousMod,
  withMainApplication,
  AndroidConfig,
} = require("@expo/config-plugins");
const fs = require("fs");
const path = require("path");

const CAR_APP_META_NAME = "com.google.android.gms.car.application";
const CAR_APP_DESC_RESOURCE = "@xml/automotive_app_desc";

// Native Kotlin sources (tracked reference impl) â†’ copied into the generated
// app source set on every prebuild. Package is io.yaver.mobile.car.
const NATIVE_PACKAGE = "io.yaver.mobile.car";
const NATIVE_SOURCES = ["YaverCarMessagingModule.kt", "YaverCarMessagingPackage.kt"];
const REPLY_RECEIVER_NAME = "io.yaver.mobile.car.YaverCarReplyReceiver";

// res/xml/automotive_app_desc.xml â€” declares a NOTIFICATION (messaging) app.
// Note the absence of any <uses name="media"/> or template entry: that keeps
// us in the entitlement-free messaging lane.
const AUTOMOTIVE_APP_DESC_XML = `<?xml version="1.0" encoding="utf-8"?>
<!-- Generated by withAndroidAutoMessaging.js. Declares Yaver as a
     notification-based Android Auto messaging app (no entitlement). -->
<automotiveApp>
    <uses name="notification" />
</automotiveApp>
`;

// ---- Android: <meta-data> in <application> --------------------------------
function withCarAppMetaData(config) {
  return withAndroidManifest(config, (cfg) => {
    const app = AndroidConfig.Manifest.getMainApplicationOrThrow(cfg.modResults);
    app["meta-data"] = app["meta-data"] || [];
    const exists = app["meta-data"].some(
      (m) => m.$ && m.$["android:name"] === CAR_APP_META_NAME,
    );
    if (!exists) {
      app["meta-data"].push({
        $: {
          "android:name": CAR_APP_META_NAME,
          "android:resource": CAR_APP_DESC_RESOURCE,
        },
      });
    }
    return cfg;
  });
}

// ---- Android: res/xml/automotive_app_desc.xml -----------------------------
function withAutomotiveAppDescResource(config) {
  return withDangerousMod(config, [
    "android",
    (cfg) => {
      const resXmlDir = path.join(
        cfg.modRequest.platformProjectRoot,
        "app",
        "src",
        "main",
        "res",
        "xml",
      );
      fs.mkdirSync(resXmlDir, { recursive: true });
      fs.writeFileSync(
        path.join(resXmlDir, "automotive_app_desc.xml"),
        AUTOMOTIVE_APP_DESC_XML,
        "utf8",
      );
      return cfg;
    },
  ]);
}

// ---- Android: copy native Kotlin sources into the app source set ----------
// `expo prebuild --clean` regenerates android/, so we re-copy on every prebuild
// (mirrors withMeshTunnel.js::withMeshIosSources). Destination is the package
// dir io/yaver/mobile/car under the app's kotlin/java source root.
function withNativeCarSources(config) {
  return withDangerousMod(config, [
    "android",
    (cfg) => {
      const root = cfg.modRequest.projectRoot;
      const src = path.join(root, "native-androidauto", "android");
      const pkgDir = NATIVE_PACKAGE.split(".").join(path.sep);
      // Expo RN apps put MainApplication.kt under
      // app/src/main/java/<pkg>/; we place the car package as a sibling.
      const destDir = path.join(
        cfg.modRequest.platformProjectRoot,
        "app",
        "src",
        "main",
        "java",
        pkgDir,
      );
      fs.mkdirSync(destDir, { recursive: true });
      for (const f of NATIVE_SOURCES) {
        const from = path.join(src, f);
        if (fs.existsSync(from)) fs.copyFileSync(from, path.join(destDir, f));
      }
      return cfg;
    },
  ]);
}

// ---- Android: register the reply <receiver> in the manifest ---------------
function withCarReplyReceiver(config) {
  return withAndroidManifest(config, (cfg) => {
    const app = AndroidConfig.Manifest.getMainApplicationOrThrow(cfg.modResults);
    app.receiver = app.receiver || [];
    const exists = app.receiver.some(
      (r) => r.$ && r.$["android:name"] === REPLY_RECEIVER_NAME,
    );
    if (!exists) {
      app.receiver.push({
        $: {
          "android:name": REPLY_RECEIVER_NAME,
          "android:exported": "false",
        },
      });
    }
    return cfg;
  });
}

// ---- Android: add YaverCarMessagingPackage() to MainApplication -----------
// Inserts the package into the getPackages() list returned by the generated
// MainApplication.kt. Idempotent: skips if already present.
function withCarPackageRegistration(config) {
  return withMainApplication(config, (cfg) => {
    let contents = cfg.modResults.contents;
    const importLine = `import ${NATIVE_PACKAGE}.YaverCarMessagingPackage`;
    if (!contents.includes(importLine)) {
      // Add the import after the package declaration line.
      contents = contents.replace(
        /(^package [^\n]+\n)/m,
        `$1${importLine}\n`,
      );
    }
    if (!contents.includes("YaverCarMessagingPackage()")) {
      // Expo's RN template builds packages via PackageList(this).packages then
      // adds custom ones in a `.apply { add(...) }` block. Hook that block.
      if (/PackageList\(this\)\.packages\.apply\s*\{/.test(contents)) {
        contents = contents.replace(
          /(PackageList\(this\)\.packages\.apply\s*\{)/,
          `$1\n              add(YaverCarMessagingPackage())`,
        );
      } else if (/val packages = PackageList\(this\)\.packages/.test(contents)) {
        // Older template: mutate the list right after it's created.
        contents = contents.replace(
          /(val packages = PackageList\(this\)\.packages\n)/,
          `$1            packages.add(YaverCarMessagingPackage())\n`,
        );
      }
      // If neither shape matches we leave a marker so the human finisher can
      // wire it; the JS side still falls back to expo-notifications.
    }
    cfg.modResults.contents = contents;
    return cfg;
  });
}

module.exports = function withAndroidAutoMessaging(config) {
  config = withCarAppMetaData(config);
  config = withAutomotiveAppDescResource(config);
  config = withNativeCarSources(config);
  config = withCarReplyReceiver(config);
  config = withCarPackageRegistration(config);
  return config;
};

